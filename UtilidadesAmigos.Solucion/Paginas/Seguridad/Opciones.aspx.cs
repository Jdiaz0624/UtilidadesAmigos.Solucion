        }

        protected void btnSiguiente_Click(object sender, ImageClickEventArgs e)
        {
            CurrentPage += 1;
            MostrarListadoOpcionesSistema();
        }

        protected void btnUltimo_Click(object sender, ImageClickEventArgs e)
        {
